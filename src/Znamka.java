public class Znamka {
    private double znamka;
    private int vaha;

    public Znamka(double znamka, int vaha) {
        this.znamka = znamka;
        this.vaha = vaha;
    }

    public double getZnamka() {
        return znamka;
    }

    public void setZnamka(double znamka) {
        this.znamka = znamka;
    }

    public int getVaha() {
        return vaha;
    }

    public void setVaha(int vaha) {
        this.vaha = vaha;
    }
}
